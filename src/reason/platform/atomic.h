class Counter
{
public:

	int Count;
	Counter():Count(0) {}

	Counter & operator ++()
	{
		Increment();
		return *this;
	}

	Counter & operator --()
	{
		Decrement();
		return *this;
	}

	void Increment() {Atomic::Inc(&Count);}
	void Decrement() {Atomic::Dec(&Count);};
};

